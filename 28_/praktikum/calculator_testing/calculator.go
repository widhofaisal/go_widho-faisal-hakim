package calculator_testing

func Add(number_a, number_b int) int64 {
	a, b := int64(number_a), int64(number_b)
	return a + b
}

func Sub(number_a, number_b int) int64 {
	a, b := int64(number_a), int64(number_b)
	return a - b
}

func Mult(number_a, number_b int) int64 {
	a, b := int64(number_a), int64(number_b)
	return a * b
}

func Div(number_a, number_b int) int64 {
	a, b := int64(number_a), int64(number_b)
	return a / b
}


